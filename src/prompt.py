def build_system_prompt(client_data: dict | None, orders: list | None,
                        base_prompt: str | None = None) -> str:
    # base_prompt (from agent_config, editable in the dashboard) overrides the code default
    prompt = base_prompt if base_prompt else SYSTEM_PROMPT_BASE

    if client_data:
        name = client_data.get("name", "")
        prompt += f"\n\nДАНЫЕ КЛИЕНТА:\nИмя: {name or '—'}\n"
        if name:
            prompt += (
                f"ОБЯЗАТЕЛЬНО обращайся к клиенту по имени «{name}» — "
                f"поздоровайся по имени в первом же ответе.\n"
            )
        if client_data.get("company"):
            prompt += f"Компания: {client_data['company']}\n"
        if client_data.get("city"):
            prompt += f"Город: {client_data['city']}\n"
        if client_data.get("phone"):
            prompt += f"Телефон: {client_data['phone']}\n"

        if orders:
            last = orders[0]
            items = last.get("items", [])
            prompt += f"\nКлиент — ПОСТОЯННЫЙ, всего заказов в базе: {len(orders)}.\n"
            prompt += (
                f"ПОСЛЕДНИЙ ЗАКАЗ № {last.get('number', '—')} от {last.get('date', '—')} "
                f"на сумму {last.get('total', 0)} грн.\n"
            )
            if items:
                items_str = ", ".join(
                    f"{i.get('name', '?')} {i.get('qty', '?')} шт" for i in items
                )
                prompt += f"Состав последнего заказа: {items_str}.\n"
                prompt += (
                    "Если тематика совпадает — предложи повторить именно эти позиции.\n"
                )
            else:
                # We don't have line items (BAS doesn't expose order contents via API)
                prompt += (
                    "ВАЖНО: состав последнего заказа неизвестен (нет данных по позициям). "
                    "НЕ придумывай конкретные товары и количества! "
                    "Можешь сослаться на сам факт прошлого заказа и спросить, "
                    "что именно нужно сейчас: «Бажаєте повторити останнє замовлення? "
                    "Підкажіть, що саме потрібно».\n"
                )
            prompt += "Не спрашивай данные которые уже знаешь (имя, компания, телефон).\n"
        else:
            prompt += "Клиент есть в базе, но заказов ещё не было.\n"
    else:
        prompt += "\n\nКлиент — НОВЫЙ, в базе не найден. Сначала помоги с товаром.\n"

    return prompt
